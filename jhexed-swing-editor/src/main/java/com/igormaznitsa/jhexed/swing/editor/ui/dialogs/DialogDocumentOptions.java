  private void buttonChangeImageFileActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonChangeImageFileActionPerformed
    final JFileChooser fileChooser = new JFileChooser();
    fileChooser.setAcceptAllFileFilterUsed(true);
    fileChooser.setFileFilter(Utils.SVG_FILE_FILTER);

    if (fileChooser.showOpenDialog(this) == JFileChooser.APPROVE_OPTION) {
      final File file = fileChooser.getSelectedFile();
      final SVGImage svgImage;
      try {
        svgImage = new SVGImage(file);
        this.buttonChangeImageFile.setIcon(this.value.setImage(svgImage));
      }
      catch (Exception ex) {
        Log.error("Can't load image ["+file+']', ex);
        JOptionPane.showMessageDialog(this, "Can't load the image for error", "Error", JOptionPane.ERROR_MESSAGE);
        return;
      }
    }

  }//GEN-LAST:event_buttonChangeImageFileActionPerformed

