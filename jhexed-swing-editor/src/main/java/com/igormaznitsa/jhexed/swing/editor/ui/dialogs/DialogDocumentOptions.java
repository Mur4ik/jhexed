  private void load() {
    switch (this.value.getHexOrientation()) {
      case HexEngine.ORIENTATION_HORIZONTAL: {
        this.buttonHorizontal.setSelected(true);
      }
      break;
      case HexEngine.ORIENTATION_VERTICAL: {
        this.buttonVertical.setSelected(true);
      }
      break;
      default:
        throw new Error("Unsupported value");
    }

    this.spinnerLineWidth.setValue(this.value.getLineWidth());
    this.spinnerNumberRows.setValue(this.value.getRows());
    this.spinnerNumberColumns.setValue(this.value.getColumns());

    this.buttonChangeImageFile.setIcon(this.value.setImage(this.value.getImage()));
    this.buttonBorderColor.setIcon(this.value.setColor(this.value.getColor()));
    
    this.textCommentary.setText(this.value.getCommentary());
  }

  private void save() {
    if (this.buttonHorizontal.isSelected()) {
      this.value.setHexOrientation(HexEngine.ORIENTATION_HORIZONTAL);
    }
    else if (this.buttonVertical.isSelected()) {
      this.value.setHexOrientation(HexEngine.ORIENTATION_VERTICAL);
    }
    else {
      throw new Error("Unknown state");
    }
    this.value.setColumns((Integer) this.spinnerNumberColumns.getValue());
    this.value.setRows((Integer) this.spinnerNumberRows.getValue());
    this.value.setLineWidth((Float) this.spinnerLineWidth.getValue());
    
    this.value.setCommentary(this.textCommentary.getText());
  }

